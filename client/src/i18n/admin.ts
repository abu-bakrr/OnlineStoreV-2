export type TranslationKey = keyof typeof adminTranslations['ru']

export function t(lang: AdminLang, key: TranslationKey): string {
  return (adminTranslations[lang] as any)?.[key] ?? (adminTranslations['ru'] as any)[key] ?? key
}

export function getAdminLang(): AdminLang {
  return (localStorage.getItem('admin_lang') as AdminLang) || 'ru'
}

export function setAdminLang(lang: AdminLang) {
  localStorage.setItem('admin_lang', lang)
}
